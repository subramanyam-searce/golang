// Write down what these print:
// ● fmt.Println(true && true)
// Todd McLeod - Learn To Code Go on Udemy - Part 1 - Page 38
// ● fmt.Println(true && false)
// ● fmt.Println(true || true)
// ● fmt.Println(true || false)
// ● fmt.Println(!true)

package main

func main() {
	// ● fmt.Println(true && true)
	//true

	// ● fmt.Println(true && false)
	//false

	// ● fmt.Println(true || true)
	//true

	// ● fmt.Println(true || false)
	//true

	// ● fmt.Println(!true)
	//false
}
